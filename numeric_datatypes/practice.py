# 3 data types:int,float and complex
# computations involving integers are 
# faster than floating point numbers
a = 12
b = 3
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a // b) 
# integer division.rounded down towards minus infinity
print(a % b)
# modulo: remainder operator
print(a + b / 3 - 4 * 12)
# PEMDAS
print((((a + b) / 3) - 4) * 12)
# multiplication and division have
# equal precedence.The same applies to addition
# and substraction
# expressions with equal precedence are evaluated
# from left to right
print(a / b)